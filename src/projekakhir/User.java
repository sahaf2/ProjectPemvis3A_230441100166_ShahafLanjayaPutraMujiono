private void loadUserOrderHistory(int userId) {
    DefaultTableModel model = (DefaultTableModel) userOrderTable.getModel(); // userOrderTable adalah JTable di tab histori order
    model.setRowCount(0); // Hapus data lama

    String sql = "SELECT order_item_id, order_id, product_id, quantity, price FROM order_items WHERE user_id = ?";
    try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
        pstmt.setInt(1, userId);
        try (ResultSet rs = pstmt.executeQuery()) {
            while (rs.next()) {
                int orderItemId = rs.getInt("order_item_id");
                int orderId = rs.getInt("order_id");
                int productId = rs.getInt("product_id");
                int quantity = rs.getInt("quantity");
                double price = rs.getDouble("price");
                
                model.addRow(new Object[]{orderItemId, orderId, productId, quantity, price});
            }
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Gagal memuat histori order: " + e.getMessage());
    }
}
