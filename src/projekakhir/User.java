private void loadProductData(int idProduk, JTextField textFieldNama, JTextArea textAreaDeskripsi, JTextField textFieldStok, JTextField textFieldHarga, JLabel lblGambar) {
    String sql = "SELECT nama_produk, deskripsi, stok, harga, image_url FROM produk WHERE id_produk = ?";

    try (Connection conn = koneksi.getConnection();
         PreparedStatement pstmt = conn.prepareStatement(sql)) {

        pstmt.setInt(1, idProduk);  // Set ID produk sesuai yang dipilih

        try (ResultSet rs = pstmt.executeQuery()) {
            if (rs.next()) {
                // Mengisi data dari ResultSet ke komponen yang diberikan
                textFieldNama.setText(rs.getString("nama_produk"));
                textAreaDeskripsi.setText(rs.getString("deskripsi"));
                textFieldStok.setText(String.valueOf(rs.getInt("stok")));
                textFieldHarga.setText(String.valueOf(rs.getDouble("harga")));

                // Mengambil data gambar dan menampilkannya di JLabel
                byte[] imgData = rs.getBytes("image_url");
                if (imgData != null) {
                    ImageIcon productImage = new ImageIcon(imgData);
                    Image img = productImage.getImage().getScaledInstance(200, 300, Image.SCALE_SMOOTH);
                    lblGambar.setIcon(new ImageIcon(img));  // Menampilkan gambar di JLabel
                } else {
                    lblGambar.setIcon(null);  // Menangani kasus jika gambar tidak ditemukan
                    JOptionPane.showMessageDialog(this, "Gambar tidak ditemukan di database!");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Data produk dengan ID " + idProduk + " tidak ditemukan!");
            }
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengambil data: " + e.getMessage());
    }
}



private void updateStok(int idProduk, int jumlahBeli) {
    String querySelect = "SELECT stok FROM produk WHERE id_produk = ?";
    String queryUpdate = "UPDATE produk SET stok = ? WHERE id_produk = ?";

    try (Connection conn = koneksi.getConnection();
         PreparedStatement stmtSelect = conn.prepareStatement(querySelect);
         PreparedStatement stmtUpdate = conn.prepareStatement(queryUpdate)) {

        // Ambil stok saat ini
        stmtSelect.setInt(1, idProduk);
        ResultSet rs = stmtSelect.executeQuery();

        // Periksa apakah produk ditemukan
        if (rs.next()) {
            int stokSaatIni = rs.getInt("stok");

            // Validasi jumlah beli
            if (jumlahBeli <= 0) {
                JOptionPane.showMessageDialog(this, "Jumlah beli tidak valid.");
                return;
            }

            int stokBaru = stokSaatIni - jumlahBeli;

            // Periksa apakah stok mencukupi
            if (stokBaru >= 0) {
                // Update stok di database
                stmtUpdate.setInt(1, stokBaru);
                stmtUpdate.setInt(2, idProduk);
                int rowsAffected = stmtUpdate.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Pembelian berhasil untuk produk ID " + idProduk + ". Stok baru: " + stokBaru);
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal memperbarui stok.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Stok tidak mencukupi untuk produk ID " + idProduk);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Produk dengan ID " + idProduk + " tidak ditemukan.");
        }

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengurangi stok: " + e.getMessage());
    }
}

private int createOrder(int userId, double totalPrice, String status) {
    String queryOrder = "INSERT INTO `orders` (user_id, total_price, order_status) VALUES (?, ?, ?)";
    System.out.println("Query Order: " + queryOrder);
    try (PreparedStatement stmtOrder = conn.prepareStatement(queryOrder, Statement.RETURN_GENERATED_KEYS)) {
        stmtOrder.setInt(1, userId);
        stmtOrder.setDouble(2, totalPrice);
        stmtOrder.setString(3, status);
        stmtOrder.executeUpdate();

        try (ResultSet generatedKeys = stmtOrder.getGeneratedKeys()) {
            if (generatedKeys.next()) {
                return generatedKeys.getInt(1);  // Mengembalikan order_id yang baru dibuat
            }
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Gagal membuat pesanan: " + e.getMessage());
    }
    return -1;
}

private void addOrderItem(int orderId, int productId, int quantity, double price) {
    String queryOrderItem = "INSERT INTO order_items (order_id, id_produk, quantity, price) VALUES (?, ?, ?, ?)";
    try (PreparedStatement stmtOrderItem = conn.prepareStatement(queryOrderItem)) {
        stmtOrderItem.setInt(1, orderId);    // Untuk kolom order_id
        stmtOrderItem.setInt(2, productId); // Untuk kolom id_produk
        stmtOrderItem.setInt(3, quantity);  // Untuk kolom quantity
        stmtOrderItem.setDouble(4, price);  // Untuk kolom price
        stmtOrderItem.executeUpdate();
        System.out.println("Item berhasil ditambahkan ke order_items.");
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Gagal menambahkan item pesanan: " + e.getMessage());
    }
}





