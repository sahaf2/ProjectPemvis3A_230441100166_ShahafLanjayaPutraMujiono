    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
JTextField[] jumlahFields = {tf1, tf2, tf3, tf4, tf5, tf6};  // TextField untuk jumlah pembelian masing-masing produk
    JCheckBox[] checkboxes = {cb1, cb2, cb3, cb4, cb5, cb6};      // Checkbox untuk memilih produk
    int[] productIds = {1, 2, 3, 4, 5, 6};                       // ID dari produk masing-masing checkbox

    double totalPrice = 0;
    try {
        for (int i = 0; i < checkboxes.length; i++) {
            if (checkboxes[i].isSelected()) {
                int idProduk = productIds[i];
                String jumlahText = jumlahFields[i].getText();
                if (jumlahText.isEmpty() || !jumlahText.matches("\\d+")) {
                    JOptionPane.showMessageDialog(this, "Masukkan jumlah pembelian yang valid pada produk " + (i + 1));
                    return;
                }
                int jumlahBeli = Integer.parseInt(jumlahText);

                // Ambil harga produk
                double hargaProduk = Double.parseDouble(jumlahFields[i].getText());
                totalPrice += hargaProduk * jumlahBeli;
                // Debugging sebelum update stok
                System.out.println("Updating stock for productId: " + idProduk + ", quantity: " + jumlahBeli);

                // Update stok produk
                updateStok(idProduk, jumlahBeli);
            }
        }

        // Membuat order baru
        int userId = 1;  // Sesuaikan dengan ID user yang sedang login
        int orderId = createOrder(userId, totalPrice, "Pending");
        
        // Debugging sebelum menambahkan item order
        System.out.println("Order created with orderId: " + orderId);

        // Menambahkan item order ke tabel order_item
        for (int i = 0; i < checkboxes.length; i++) {
            if (checkboxes[i].isSelected()) {
                int idProduk = productIds[i];
                int jumlahBeli = Integer.parseInt(jumlahFields[i].getText());
                double hargaProduk = Double.parseDouble(jumlahFields[i].getText());

                addOrderItem(orderId, idProduk, jumlahBeli, hargaProduk);
            }
        }

        JOptionPane.showMessageDialog(this, "Pesanan berhasil dibuat.");
//        // Menampilkan frame beli setelah pemesanan
//        new User(int currentUserId).setVisible(true);
//        this.dispose();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
    }


    }//GEN-LAST:event_jButton1ActionPerformed


